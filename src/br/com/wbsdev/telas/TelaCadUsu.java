    public void preencherTabela() throws SQLException{
        String [] colunas = {"Id","Nome","Telefone","Login","Senha","Tipo"};
        ArrayList linhas = usuDao.buscarTodosParaPreencherTabela();
        
        ModeloTabelaUsuario mu = new ModeloTabelaUsuario(linhas, colunas);
        jtbUsuarios.setModel(mu);
        
         jtbUsuarios.getColumnModel().getColumn(0).setPreferredWidth(70);
          jtbUsuarios.getColumnModel().getColumn(0).setResizable(false);
          jtbUsuarios.getColumnModel().getColumn(1).setPreferredWidth(200);
          jtbUsuarios.getColumnModel().getColumn(1).setResizable(false);
          jtbUsuarios.getColumnModel().getColumn(2).setPreferredWidth(120);
          jtbUsuarios.getColumnModel().getColumn(2).setResizable(false);
          jtbUsuarios.getColumnModel().getColumn(3).setPreferredWidth(100);
          jtbUsuarios.getColumnModel().getColumn(3).setResizable(false);
          jtbUsuarios.getColumnModel().getColumn(4).setPreferredWidth(100);
          jtbUsuarios.getColumnModel().getColumn(4).setResizable(false);
          jtbUsuarios.getColumnModel().getColumn(5).setPreferredWidth(100);
          jtbUsuarios.getColumnModel().getColumn(5).setResizable(false);
          
          jtbUsuarios.getTableHeader().setReorderingAllowed(false);
          jtbUsuarios.setAutoResizeMode(0);
          jtbUsuarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
    
    }
    
    public void carregarDadosDaTabelaParaTela(){
        if(jtbUsuarios.getSelectedRow() != -1){
            txtIdUsu.setText(jtbUsuarios.getValueAt(jtbUsuarios.getSelectedRow(), 0).toString());
            txtNome.setText(jtbUsuarios.getValueAt(jtbUsuarios.getSelectedRow(), 1).toString());
            jfmtFone.setText(retiraMascaraTelefone(jtbUsuarios.getValueAt(jtbUsuarios.getSelectedRow(), 2).toString()));
            txtLogin.setText(jtbUsuarios.getValueAt(jtbUsuarios.getSelectedRow(), 3).toString());
            pwdSenha.setText(jtbUsuarios.getValueAt(jtbUsuarios.getSelectedRow(), 4).toString());
            String tipo = jtbUsuarios.getValueAt(jtbUsuarios.getSelectedRow(), 5).toString();
            if(tipo.equals("Administrador")){
                rdAdmin.setSelected(true);
            }else if(tipo.equals("Colaborador")){
                rdColab.setSelected(true);
            }
        }
    }
    
    public String retiraMascaraTelefone(String telefone){
        StringBuilder builder = new StringBuilder(telefone);
        
        for(int i = 0; i < builder.length(); i++){
            if(builder.charAt(i) == '('|| builder.charAt(i) == ')' || builder.charAt(i) ==  '-' ){
                    builder.deleteCharAt(i);
            }
        }
        
        return telefone = builder.toString();
    }

    
    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        Usuario usu = new Usuario();
        usu.setNome(txtNome.getText());
        usu.setTelefone(jfmtFone.getText());
        usu.setLogin(txtLogin.getText());
        usu.setSenha(pwdSenha.getText());
        if(rdAdmin.isSelected()){
            usu.setTipo(rdAdmin.getText());
        }else if(rdColab.isSelected()){
            usu.setTipo(rdColab.getText());
        }
       Boolean statusCad = usuDao.cadastrar(usu);
       if(statusCad){
           JOptionPane.showMessageDialog(null, "Usu치rio cadastrado com sucesso!");
           limparCampos();
       }else{
           JOptionPane.showMessageDialog(null, "Algo deu errado! Tente cadastrar o usu치rio novamente.");
       }
    }//GEN-LAST:event_btnCadActionPerformed

